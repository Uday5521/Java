package com;

public class DefaultValueDemo {

	int a;
	byte b;
	short c;
	long d;

	public static void main(String[] args) {

		DefaultValueDemo d = new DefaultValueDemo();

		System.out.println(d.a);// 0
		System.out.println(d.b);//0
		System.out.println(d.c);//0
		System.out.println(d.d);//0
	}

}
