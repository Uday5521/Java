package Operatros;

public class AO {
	public static void main(String[] args) {
		
		int a = 5;
		int b= 10;
		
		System.out.println(a>b);//5>10 False
		System.out.println(a>=b);//5>10 False
		System.out.println(a<b);//5<10 True
		System.out.println(a<=b);//5<10 True
		System.out.println(a==b);//5==10 False
		System.out.println(a!=b);//5!=10 true
	}

}
