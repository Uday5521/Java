package ControlStatements;

public class Switch {
	public static void main(String[] args) {

		char a = 'A';
		switch (a) {
		case 'A':
			System.out.println("alphabet A");
			break;
		case 'B':
			System.out.println("alphabet B");
			break;
		case 'C':
			System.out.println("alphabet C");
			break;
		default:
			System.out.println("Invalid");
		}
	}

}
