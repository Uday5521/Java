package com;

public class ForLoop {
	public static void main(String[] args) {
		System.out.println("hello");
		System.out.println("hello");
		System.out.println("hello");
		System.out.println("hello");
		System.out.println("hello");
		System.out.println("--------------------------------------");
		for(int i=1; i<=10; i++){
			System.out.println("hello");
		}
//		1<=5 true hello i++  2
//		2<=5 true hello i++  3
//		3<=5 true hello i++  4
//		4<=5 true hello i++  5
//		5<=5 true hello i++  6
//		6<=5 false 
		System.out.println("--------------------------------------");
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		System.out.println(4);
		System.out.println(5);
		System.out.println("--------------------------------------");
		for(int i=1; i<=100; i++){
			System.out.println(i);
		}
	}

}
