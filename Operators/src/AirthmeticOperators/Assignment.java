package AirthmeticOperators;

public class Assignment {
	public static void main(String[] args) {

		int x = 10;
		System.out.println(x);
		System.out.println("-----------");

		x += 20; // 10+=20 x=10+20
		System.out.println(x);// 30
		System.out.println("-----------");

		int i = 45;
		System.out.println(i);
		System.out.println("-----------");

		i -= 13; //45-=13
		System.out.println(i);
	}

}
