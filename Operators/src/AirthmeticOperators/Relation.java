package AirthmeticOperators;

public class Relation {
	public static void main(String[] args) {

		int x = 10;
		int y = 20;
// <, <=, >, >=, ==, != 
		System.out.println(10 < 20);// 10<20
		System.out.println(x <= 5);// 10<=5
		System.out.println("--------------");

		System.out.println(y > 50);// 20>50
		System.out.println(y >= 20);// 20>=20
		System.out.println("--------------");

		System.out.println(x == y);// 10==20
		System.out.println(x != y);// 10!=20
		System.out.println("--------------");

		System.out.println(x == 10);// 10==10
		System.out.println(x != 10);// 10!=10
	}

}
