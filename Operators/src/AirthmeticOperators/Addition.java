package AirthmeticOperators;

public class Addition {
	public static void main(String[] args) {
		
		int a = 5;
		int b = 10;
		int add = a+b; // 5+10 = 15
		
		System.out.println("Addition: "+add);//15
		System.out.println("Subtraction: "+ (a-2));// 5-2 =3
		System.out.println("Multiplication: "+4*b);// 4*10 =40
		System.out.println("Division: "+10/5);//2
		System.out.println("Moduls: "+10%2); //0
	}

}
